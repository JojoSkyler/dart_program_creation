void main() {
  // Integer data type (int)
  int age = 18;

  // Double data type (double)
  double height = 5.9;

  // String data type (String)
  String name = "Joan Skyler";

  // List data type (List)
  List<String> hobbies = ["Coding", "Dancing", "Cooking"];

  // Map data type (Map)
  Map<String, dynamic> person = {
    'name': 'Felix Rich',
    'age': 18,
    'height': 6.0,
    'hobbies': ['Coding', 'Walking']
  };

  // Printing the values
  print("Age: $age");
  print("Height: $height");
  print("Name: $name");
  print("Hobbies: $hobbies");
  print("Person: $person");
}
